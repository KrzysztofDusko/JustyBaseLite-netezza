/**
 * Normalizes values from both ODBC and JsNzDriver for comparison.
 * Handles differences in type representation between the two drivers.
 */
function normalize(val) {
    if (val === null || val === undefined) return null;
    if (typeof val === 'bigint') return val.toString();

    // Normalize boolean to Netezza char representation 't'/'f'
    if (typeof val === 'boolean') return val ? 't' : 'f';

    // Date Normalization
    if (val instanceof Date) {
        return val.toISOString();
    }

    if (typeof val === 'string') {
        // 't'/'f' are already fine if we normalize boolean to char above.
        if (val === 't') return 't';
        if (val === 'f') return 'f';

        // normalize ODBC date strings "YYYY-MM-DD" or "YYYY-MM-DD HH:mm:ss"
        if (/^\d{4}-\d{2}-\d{2}$/.test(val)) {
            return val + 'T00:00:00.000Z';
        }
        if (/^\d{4}-\d{2}-\d{2} \d{2}:\d{2}:\d{2}$/.test(val)) {
            return val.replace(' ', 'T') + '.000Z';
        }

        // Remove trailing zeros from fractional seconds for consistency
        // e.g. 12:00:00.123000 -> 12:00:00.123
        if (val.includes('.')) {
            val = val.replace(/(\.\d+)/, (match, p1) => {
                const clean = p1.replace(/0+$/, '');
                return clean === '' ? '' : clean;
            });
            // Clean up if it ends with '.' due to replace
            if (val.endsWith('.')) val = val.slice(0, -1);
            // Also handle if '.' is followed by timezone like +00
            val = val.replace(/\.([+\-Z])/, '$1');
        }

        // Trim padding (ODBC returns padded NCHAR, JsNzDriver/Netezza Protocol might return varlen)
        return val.trim();
    }

    // Handle time objects
    if (typeof val === 'object' && val.hours !== undefined) {
        return `${String(val.hours).padStart(2, '0')}:${String(val.minutes).padStart(2, '0')}:${String(val.seconds).padStart(2, '0')}`;
    }

    return val.toString();
}

/**
 * Compares query results from JsNzDriver and ODBC.
 * @param {Object} nzReader - DataReader from JsNzDriver
 * @param {Array} odbcResult - Result array from ODBC query
 */
async function compareResults(nzReader, odbcResult) {
    let rowIdx = 0;
    while (await nzReader.read()) {
        if (rowIdx >= odbcResult.length) break;

        const odbcRow = odbcResult[rowIdx];

        // Verify column count match
        expect(nzReader.fieldCount).toBe(odbcRow.length);

        for (let j = 0; j < nzReader.fieldCount; j++) {
            const valJs = normalize(nzReader.getValue(j));
            const valOdbc = normalize(odbcRow[j]);

            if (valJs !== valOdbc) {
                const nJs = Number(valJs);
                const nOdbc = Number(valOdbc);
                if (!isNaN(nJs) && !isNaN(nOdbc)) {
                    // Handle signed vs unsigned ByteInt (JS -128 vs ODBC 128)
                    if (nJs < 0 && nOdbc === nJs + 256) continue;

                    // Handle ODBC driving clamping BigInt to Int32
                    if ((nOdbc === 2147483647 && nJs > nOdbc) || (nOdbc === -2147483648 && nJs < nOdbc)) continue;

                    expect(nJs).toBeCloseTo(nOdbc, 3);
                    continue;
                }

                // Allow for ODBC truncation of strings (relax length check)
                if (typeof valJs === 'string' && typeof valOdbc === 'string') {
                    if (valJs.length > valOdbc.length && valOdbc.length > 5 && valJs.startsWith(valOdbc)) {
                        continue;
                    }

                    // Attempt date/time approx match (within 1 second)
                    const dJs = new Date(valJs);
                    const dOdbc = new Date(valOdbc);
                    if (!isNaN(dJs) && !isNaN(dOdbc)) {
                        if (Math.abs(dJs.getTime() - dOdbc.getTime()) < 5000) continue; // Allow 5s variance for system views
                    }
                }
            }

            try {
                expect(valJs).toBe(valOdbc);
            } catch (e) {
                console.log(`Failed Row ${rowIdx} Col Index ${j} (${nzReader.getName(j)}): JS '${valJs}' != ODBC '${valOdbc}'`);
                throw e;
            }
        }
        rowIdx++;
    }
    expect(rowIdx).toBe(odbcResult.length);
}

/**
 * Creates connection setup and teardown hooks for test suites.
 * @returns {Object} Object containing getConnections function
 */
function createConnectionHooks() {
    let nzConn = null;
    let odbcConn = null;

    beforeAll(async () => {
        nzConn = new NzConnection(config);
        await nzConn.connect();

        try {
            odbcConn = await odbc.connect({
                connectionString,
                fetchArray: true
            });
        } catch (e) {
            console.error("ODBC Connection Error. Check installed driver and connection string.");
            throw e;
        }
    });

    afterAll(async () => {
        if (nzConn) nzConn.close();
        if (odbcConn) await odbcConn.close();
    });

    return {
        getConnections: () => ({ nzConn, odbcConn })
    };
}

/**
 * Creates a test case that compares JsNzDriver results with ODBC.
 * @param {Function} getConnections - Function that returns { nzConn, odbcConn }
 * @returns {Function} Test function for use with test.each
 */
function createComparisonTest(getConnections) {
    return async (query) => {
        // console.log('Running query:', query);
        const { nzConn, odbcConn } = getConnections();

        const nzCmd = nzConn.createCommand(query);
        const nzReader = await nzCmd.executeReader();
        const odbcResult = await odbcConn.query(query);

        try {
            await compareResults(nzReader, odbcResult);
        } finally {
            await nzReader.close();
        }
    };
}

